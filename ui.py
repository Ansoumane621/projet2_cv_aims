class MiniBarChart(QWidget):
    """
    Custom widget for displaying vertical bar charts.
    Used for class distribution, direction split, and confidence charts.
    """
    
    def __init__(self, parent=None):
        super().__init__(parent)
        self._labels = []    # X-axis labels
        self._values = []    # Bar values
        self._colors = []    # Bar colors
        self.setMinimumHeight(170)

    def set_data(self, labels, values, colors=None):
        """Set chart data and trigger redraw."""
        self._labels = labels
        self._values = values
        self._colors = colors or ["#2563eb"] * len(labels)
        self.update()  # Trigger paintEvent

    def paintEvent(self, _):
        """
        Main drawing method - called automatically by Qt.
        Draws a bar chart with:
        - Background grid (horizontal lines)
        - Vertical scale (values)
        - Gradient-filled bars with rounded corners
        - Labels below each bar
        """
        if not self._values:
            return
        p = QPainter(self)
        p.setRenderHint(QPainter.Antialiasing)  # Edge smoothing
        W, H = self.width(), self.height()
        # Margins: PL=left, PR=right, PT=top, PB=bottom
        PL, PR, PT, PB = 42, 12, 20, 32
        dw = W - PL - PR
        dh = H - PT - PB
        mx = max(self._values) or 1
        n  = len(self._values)
        gap = 8
        bw  = max(10, (dw - gap * (n + 1)) // n)

        # Draw grid (5 horizontal lines)
        p.setPen(QPen(QColor("#f3f4f6"), 1))
        for i in range(5):
            y = PT + dh * i // 4
            p.drawLine(PL, y, W - PR, y)

        # Draw vertical scale
        p.setPen(QColor("#9ca3af"))
        p.setFont(QFont("Segoe UI", 8))
        for i in range(5):
            v = mx * (4 - i) / 4
            y = PT + dh * i // 4
            p.drawText(0, y - 6, PL - 4, 14, Qt.AlignRight, str(int(v)))

        # Draw bars
        for i, (lbl, val, col) in enumerate(
                zip(self._labels, self._values, self._colors)):
            x  = PL + gap + i * (bw + gap)
            bh = int(dh * val / mx)           # Proportional height
            y  = PT + dh - bh                  # Y position (bottom-up)
            
            # Gradient color for bar
            grad = QLinearGradient(x, y, x, y + bh)
            grad.setColorAt(0, QColor(col))
            c2 = QColor(col); c2.setAlpha(140)
            grad.setColorAt(1, c2)
            p.setBrush(QBrush(grad))
            p.setPen(Qt.NoPen)
            
            # Rounded rectangle bar
            path = QPainterPath()
            path.addRoundedRect(QRectF(x, y, bw, bh), 4, 4)
            p.drawPath(path)

            # Value above bar
            p.setPen(QColor("#374151"))
            p.setFont(QFont("Segoe UI", 8, QFont.Bold))
            p.drawText(x, max(4, y - 16), bw, 14, Qt.AlignCenter, str(val))

            # Label below bar
            p.setPen(QColor("#6b7280"))
            p.setFont(QFont("Segoe UI", 7))
            p.drawText(x - 4, H - PB + 6, bw + 8, 20,
                       Qt.AlignCenter, lbl[:7])
        p.end()


