# LEVEL 8: HeatmapWidget - Spatial heat map (22x22 grid)
# -----------------------------------------------------------------------------
class HeatmapWidget(QWidget):
    """
    Displays a 22x22 spatial heatmap of detected object positions.
    Color coding: dark blue (<33%) → cyan (33-66%) → green (>66%)
    """
    G = 22  # Grid size (22x22 cells)

    def __init__(self, parent=None):
        super().__init__(parent)
        self._grid = [[0]*self.G for _ in range(self.G)]  # 2D count grid
        self._max  = 1
        self.setMinimumSize(260, 190)

    def set_points(self, points, w=640, h=480):
        """
        Convert (cx, cy) coordinates to grid cell positions.
        For each point, increment the corresponding grid cell.
        """
        G = self.G
        self._grid = [[0]*G for _ in range(G)]
        for cx, cy in points:
            gi = min(G-1, max(0, int(cx * G / w)))  # Column index
            gj = min(G-1, max(0, int(cy * G / h)))  # Row index
            self._grid[gj][gi] += 1
        self._max = max(max(row) for row in self._grid) or 1
        self.update()

    def paintEvent(self, _):
        """
        Draws the heatmap with color gradient based on density:
        - Blue (< 33%): low density
        - Cyan (33-66%): medium density
        - Green (> 66%): high density
        Opacity increases with density.
        """
        p = QPainter(self)
        p.setRenderHint(QPainter.Antialiasing)
        W, H = self.width(), self.height()
        G    = self.G
        cw, ch = W/G, H/G
        
        # Black background
        p.fillRect(0, 0, W, H, QColor("#111827"))
        p.setPen(QPen(QColor(255,255,255,10), 1))
        
        # Grid lines
        for i in range(G+1):
            p.drawLine(int(i*cw), 0, int(i*cw), H)
            p.drawLine(0, int(i*ch), W, int(i*ch))
        
        # Colored cells
        for j in range(G):
            for i in range(G):
                ratio = self._grid[j][i] / self._max
                if ratio < 0.001:
                    continue
                if ratio < 0.33:
                    t = ratio / 0.33
                    r,g,b = 0, int(100+t*155), int(200+t*55)
                elif ratio < 0.66:
                    t = (ratio-0.33)/0.33
                    r,g,b = int(t*255), 255, int(255-t*255)
                else:
                    t = (ratio-0.66)/0.34
                    r,g,b = 255, int(255-t*255), 0
                alpha = min(255, int(ratio * 240))
                p.fillRect(int(i*cw), int(j*ch),
                           int(cw)+1, int(ch)+1, QColor(r, g, b, alpha))
        p.end()


