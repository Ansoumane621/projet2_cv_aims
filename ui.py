# LEVEL 7: MiniPieChart - Pie/donut chart widget
# -----------------------------------------------------------------------------
class MiniPieChart(QWidget):
    """
    Custom widget for displaying pie charts.
    Used for traffic mix visualization.
    """
    
    def __init__(self, parent=None):
        super().__init__(parent)
        self._labels = []
        self._values = []
        self._colors = []
        self.setMinimumSize(210, 180)

    def set_data(self, labels, values, colors=None):
        self._labels = labels
        self._values = values
        self._colors = colors or list(CLASS_PALETTE.values())
        self.update()

    def paintEvent(self, _):
        """
        Draws a pie chart with:
        - Colored sectors proportional to values
        - White borders between sectors
        - Legend with percentages on the right
        """
        if not self._values:
            return
        p = QPainter(self)
        p.setRenderHint(QPainter.Antialiasing)
        W, H   = self.width(), self.height()
        total  = sum(self._values) or 1
        r      = min(W // 2, H) // 2 - 14    # Circle radius
        cx, cy = r + 14, H // 2              # Circle center

        # Draw sectors
        angle = -90 * 16                      # Start angle (in 1/16 degree units)
        for i, (lbl, val) in enumerate(zip(self._labels, self._values)):
            span = int(360 * 16 * val / total)  # Sector angle
            p.setBrush(QBrush(QColor(self._colors[i % len(self._colors)])))
            p.setPen(QPen(QColor("#ffffff"), 2))  # White border between sectors
            p.drawPie(cx - r, cy - r, 2*r, 2*r, angle, span)
            angle += span

        # Draw legend
        lx = cx + r + 16
        ly = cy - len(self._labels) * 11
        p.setFont(QFont("Segoe UI", 8))
        for i, (lbl, val) in enumerate(zip(self._labels, self._values)):
            col = QColor(self._colors[i % len(self._colors)])
            p.setBrush(QBrush(col)); p.setPen(Qt.NoPen)
            p.drawRoundedRect(lx, ly + i * 20, 10, 10, 2, 2)  # Color square
            p.setPen(QColor("#374151"))
            pct = f"{100*val/total:.0f}%"
            p.drawText(lx + 14, ly + i * 20, 110, 14,
                       Qt.AlignLeft | Qt.AlignVCenter, f"{lbl}  {pct}")
        p.end()


