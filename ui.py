def _safe_float(val: str, lo: float = -1e6, hi: float = 1e6,
                default: float = 0.0) -> float:
    """
    SECURITY FIX — safe numeric conversion.
    Parse val as float and clamp it to [lo, hi].
    Returns default on any parse error or if the value is NaN / Inf.
    Prevents chart rendering crashes from malformed CSV values.
    """
    try:
        f = float(val)
        if not math.isfinite(f):
            return default
        return max(lo, min(hi, f))
    except (ValueError, TypeError):
        return default


def _safe_int(val: str, lo: int = 0, hi: int = 10_000_000,
              default: int = 0) -> int:
    """SECURITY FIX — safe integer conversion with bounds."""
    try:
        return max(lo, min(hi, int(val)))
    except (ValueError, TypeError):
        return default


def _sanitise_cell(val: str, max_len: int = 256) -> str:
    """
    SECURITY FIX — truncate and strip control characters from CSV cell values
    before displaying them in the QTableWidget.
    """
    val = str(val)[:max_len]
    # Remove ASCII control characters (except tab/newline which str() handles)
    val = "".join(ch for ch in val if ch >= " " or ch in "\t\n")
    return val


