# LEVEL 9: TrajectoryCanvas - Object trajectory visualization
# -----------------------------------------------------------------------------
class TrajectoryCanvas(QWidget):
    """
    Displays movement trajectories of tracked objects.
    Each trajectory shows:
    - Progressive line with increasing opacity
    - Final position marked with a circle (head)
    """
    
    def __init__(self, parent=None):
        super().__init__(parent)
        self._tracks = {}  # {track_id: {"cls": class, "points": [(x,y), ...]}}
        self.setMinimumSize(260, 190)

    def set_tracks(self, tracks):
        self._tracks = tracks
        self.update()

    def paintEvent(self, _):
        """
        Draws each trajectory:
        - Progressive line with increasing opacity toward the end
        - Final point (head) marked with a colored circle
        """
        p = QPainter(self)
        p.setRenderHint(QPainter.Antialiasing)
        W, H = self.width(), self.height()
        p.fillRect(0, 0, W, H, QColor("#111827"))
        
        # Background grid
        p.setPen(QPen(QColor(255,255,255,10), 1))
        for i in range(0, W, 30):
            p.drawLine(i, 0, i, H)
        for j in range(0, H, 30):
            p.drawLine(0, j, W, j)
        
        # Trajectories
        for tid, info in self._tracks.items():
            pts = info.get("points", [])
            if len(pts) < 2:
                continue
            col = QColor(CLASS_PALETTE.get(info.get("cls","car"), "#6b7280"))
            for k in range(1, len(pts)):
                alpha = int(60 + 195 * k / len(pts))  # Increasing opacity
                pen   = QPen(QColor(col.red(), col.green(), col.blue(), alpha), 1)
                p.setPen(pen)
                x0, y0 = pts[k-1]
                x1, y1 = pts[k]
                # Scale from 640x480 to widget size
                p.drawLine(int(x0*W/640), int(y0*H/480),
                           int(x1*W/640), int(y1*H/480))
            
            # Final point (trajectory head)
            lx, ly = pts[-1]
            p.setBrush(QBrush(col))
            p.setPen(QPen(QColor("#ffffff"), 1))
            p.drawEllipse(int(lx*W/640)-3, int(ly*H/480)-3, 6, 6)
        p.end()


